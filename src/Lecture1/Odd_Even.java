package Lecture1;

public class Odd_Even {

	public static void main(String[] args) {
		// Find even/odd number
		int n = 67;    // read input 
		if(n>785) {
			System.out.println("Yes");
		}
		else {
			System.out.println("NO");
		}
		if(n%2==0) {
			System.out.println("Even number");
		}
		else {
			System.out.println("Odd number ");
		}
	}
}
